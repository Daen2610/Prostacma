    private void agregarNuevaPieza() {
    String numeroParte = txtNumeroParte.getText().trim();
    String nombre = txtNombre.getText().trim();
    String espesorStr = txtEspesor.getText().trim();
    String anchoStr = txtAncho.getText().trim();
    String especificacion = txtEspecificacion.getText().trim();
    String pesoBlankStr = txtPesoBlank.getText().trim();

    // Validaciones de campos obligatorios
    if (numeroParte.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El Número de Parte es obligatorio.", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El Nombre es obligatorio.", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (espesorStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El Espesor es obligatorio.", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (anchoStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El Ancho es obligatorio.", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (pesoBlankStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El Peso Blank es obligatorio.", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
        return;
    }

    BigDecimal espesor = null;
    BigDecimal ancho = null;
    BigDecimal pesoBlank = null;

    // Validaciones de formato numérico
    try {
        espesor = new BigDecimal(espesorStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El Espesor debe ser un número válido.", "Formato Incorrecto", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        ancho = new BigDecimal(anchoStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El Ancho debe ser un número válido.", "Formato Incorrecto", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        pesoBlank = new BigDecimal(pesoBlankStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El Peso Blank debe ser un número válido.", "Formato Incorrecto", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection conn = MyConnection.getConnection()) {
        String sql = "INSERT INTO Registros (numero_parte, nombre, espesor, ancho, especificaciones, peso_blank) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
        stmt.setString(1, numeroParte);
        stmt.setString(2, nombre);
        stmt.setBigDecimal(3, espesor);
        stmt.setBigDecimal(4, ancho);
        stmt.setString(5, especificacion);
        stmt.setBigDecimal(6, pesoBlank);

        stmt.executeUpdate();
        ResultSet rs = stmt.getGeneratedKeys();
        if (rs.next()) {
            int nuevoId = rs.getInt(1);

            // Abrir la ventana siguiente y pasar el número de parte y nombre
            AgregarProcesos pasosWindow = new AgregarProcesos(numeroParte, nombre);
            pasosWindow.setVisible(true);
            this.dispose(); // Cerrar ventana actual
        }
    } catch (SQLException e) {
        // Manejo específico para errores de clave primaria duplicada
        if (e.getSQLState().equals("23000") && e.getMessage().contains("Duplicate entry")) {
            JOptionPane.showMessageDialog(this, "El Número de Parte '" + numeroParte + "' ya existe.", "Error de Clave Duplicada", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el registro: " + e.getMessage(), "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
        }
    }
}
